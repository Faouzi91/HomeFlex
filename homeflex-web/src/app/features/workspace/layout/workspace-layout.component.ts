  protected userInitials(user: User | null): string {
    if (!user) return '?';
    return initials(user.firstName, user.lastName);
  }

  protected logout(): void {
    this.session
      .logout()
      .pipe(takeUntilDestroyed(this.destroyRef))
      .subscribe(() => this.router.navigateByUrl('/'));
  }

